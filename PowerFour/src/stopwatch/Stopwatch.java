    private void startbtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_startbtnMouseClicked
        stop = false;
        new Thread(new Runnable() {
            public void run() {
                for (int i = 0;; i++) {
                    for (min = 0; min < 60; min++) {
                        for (sec = 0; sec < 60; sec++) {
                            for (sensec = 0; sensec < 100; sensec++) {
                                if (stop) {
                                    break;
                                }
                                NumberFormat nf = new DecimalFormat("00");
                                minutes.setText(nf.format(min));
                                seconds.setText(nf.format(sec));
                                sentiseconds.setText(nf.format(sensec));
                                try {
                                    Thread.sleep(10);
                                } catch (Exception e) {
                                }
                            }
                            if (stop) {
                                break;
                            }
                        }
                        if (stop) {
                            break;
                        }
                    }
                    if (stop) {
                        break;
                    }
                }
            }
        }).start();
    }//GEN-LAST:event_startbtnMouseClicked

