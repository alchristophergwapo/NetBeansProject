    private void countDownstartbtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_countDownstartbtnMouseClicked
        // TODO add your handling code here:
        new Thread(new Runnable() {
            String time = countDownTime.getText();
            int timeR = Integer.parseInt(time);

            public void run() {
                while (timeR >= 0) {
                    countDownTime.setText(String.valueOf(timeR));
                    try {
                        timeR--;
                        Thread.sleep(1000L);    // 1000L = 1000ms = 1 second
                    } catch (InterruptedException e) {
                        //I don't think you need to do anything for your particular problem
                    }

                }
                while (timeR <= 0) {
                    countDownTime.setForeground(Color.RED);
                    countDownTime.setForeground(Color.BLUE);
                }

            }

        }).start();
    }//GEN-LAST:event_countDownstartbtnMouseClicked

