    public void currentTime() {
        new Thread(new Runnable() {

            @Override
            public void run() {
                try {
                    while (true) {
                        java.util.Date d = new java.util.Date();
                        SimpleDateFormat sdf = new SimpleDateFormat("hh:MM:ss a");
                        currentTime.setText(sdf.format(d));
                        Thread.sleep(1000);

                    }

                } catch (Exception e) {
                }
            }
        }).start();
    }

